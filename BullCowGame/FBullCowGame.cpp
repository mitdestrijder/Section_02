// receives a VALID guess, incriments turn, and returns count
FBullCowCount FBullCowGame::SubmitValidGuess(FString Guess)
{
	MyCurrentTry++;
	FBullCowCount BullCowCount;
	int32 WordLength = MyHiddenWord.length(); // assuming same length as guess

											  // loop through all letters in the hidden word
	for (int32 MHWChar = 0; MHWChar < WordLength; MHWChar++) {
		// compare letters against the guess
		for (int32 GChar = 0; GChar < WordLength; GChar++) {
			// if they match then
			if (Guess[GChar] == MyHiddenWord[MHWChar]) {
				if (MHWChar == GChar) { // if they're in the same place
					BullCowCount.Bulls++; // incriment bulls
				}
				else {
					BullCowCount.Cows++; // must be a cow
				}
			}
		}
	}
	if (BullCowCount.Bulls == WordLength)
	{
		bGameIsWon = true;
	}
	else
	{
		bGameIsWon = false;
	}
	return BullCowCount;
}

bool FBullCowGame::IsIsogram(FString Word) const
{
	// treat 0 and 1 letters words as isograms
	if (Word.length() <= 1)
	{
		return true;
	}

	Tmap<char, bool> LetterSeen; // setup tmap
	for(auto Letter : Word)		// for all letter of the word
	{
		Letter = tolower(Letter); // handle mixed case
		if (LetterSeen[Letter])	// if the letter is in the map
		{
			return false;// we do not have a isogram 
		}
		else// otherwise
		{
			LetterSeen[Letter] = true; // add letter to the map as seen 
		}
			
	}
	return true; // for cases where /0 is enterd
}



